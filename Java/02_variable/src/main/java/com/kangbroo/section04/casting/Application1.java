package com.kangbroo.section04.casting;

public class Application1 {
    public static void main(String[] args) {
        // int(4byte) -> double(8byte)
        int i2 = 12;
        double d2 = 3.3; // i2 -> 12.0

    // int(4byte) -> long(8byte)
        int i3 = 1000;
        long l3 = i3;

    // float(4byte) -> double(8byte)
        float f4 = 1.0f;
        double d4 = f4;
    }
}
