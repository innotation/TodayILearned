package com.kangbroo.practice;

public class PracApplication1 {
    public static void main(String[] args) {
        int i1 = 20;
        int i2 = 30;
        System.out.printf("더하기 결과: %d\n", i1 + i2);
        System.out.printf("빼기 결과: %d\n", i1 - i2);
        System.out.printf("곱하기 결과: %d\n", i1 * i2);
        System.out.printf("나누기 결과: %d\n", i1 / i2);
        System.out.printf("나머지 결과: %d\n", i1 % i2);
    }
}
